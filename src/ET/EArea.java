
package ET;


public class EArea {
    
    private String ruta;
    private String area;

    public EArea(String ruta, String area) {
        this.ruta = ruta;
        this.area = area;
    }

    

    public String getRuta() {
        return ruta;
    }

    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    public String getArea() {
        return area;
    }

    public void setArea(String area) {
        this.area = area;
    }

    
    
    
    
}
