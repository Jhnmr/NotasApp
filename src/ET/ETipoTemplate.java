package ET;

/**
 *
 * @author manol
 */
public class ETipoTemplate {

    private String ruta;
    private String tipo;

    public ETipoTemplate(String ruta, String tipo) {
        this.ruta = ruta;
        this.tipo = tipo;
    }

    public String getRuta() {
        return ruta;
    }

    public void setRuta(String ruta) {
        this.ruta = ruta;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

}
