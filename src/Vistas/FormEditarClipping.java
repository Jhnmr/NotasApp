    public void MostrarClipping(ET.ETemplate obj) throws IOException {

        BL.BLTemplate bltemp = new BL.BLTemplate();
        bltemp.MostrarTemp(obj);
        txtNombre.setText(obj.getNombre());
        textArea.setText(obj.getContenido());
        lblRuta.setText(obj.getArea());
        lblRuta.hide();
    }

    public void EditarClipping() throws IOException {

        String nombre = txtNombre.getText();
        String contenido = textArea.getText();
        String ruta = lblRuta.getText();

        ET.ETemplate etemp = new ET.ETemplate(nombre, true, contenido, ruta);

        BL.BLTemplate bltemp = new BL.BLTemplate();

        bltemp.EditarTemplate(etemp);
    }

